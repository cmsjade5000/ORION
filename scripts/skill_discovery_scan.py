#!/usr/bin/env python3
"""
Weekly online skill discovery scan for OpenClaw sources.

Read-only pipeline:
- Fetch official docs sitemap and official OpenClaw GitHub releases feed.
- Infer candidate capabilities via URL/title heuristics.
- Score candidates against the intake rubric dimensions.
- Write timestamped artifacts to eval/history.
- Optionally upsert an autogenerated section in the weekly shortlist doc.
"""

